USE dnf;

DROP TABLE IF EXISTS `t_match`;
CREATE TABLE `t_match` (
	`id` INT(8) UNSIGNED NOT NULL AUTO_INCREMENT,
	`type_id` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`status` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`round` TINYINT(2) UNSIGNED NOT NULL DEFAULT '0',
	`tm_date` SMALLINT(4) NOT NULL DEFAULT '-1',
	`tm_signup_beg` SMALLINT(4) UNSIGNED NOT NULL DEFAULT '0',
	`tm_signup_end` SMALLINT(4) UNSIGNED NOT NULL DEFAULT '0',
	`tm_begin` SMALLINT(4) UNSIGNED NOT NULL DEFAULT '0',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

DROP TABLE IF EXISTS `t_match_pl`;
CREATE TABLE `t_match_pl` (
	`id` INT(8) UNSIGNED NOT NULL,
	`uid` INT(8) UNSIGNED NOT NULL DEFAULT '0',
	`uid_op` INT(8) UNSIGNED NOT NULL DEFAULT '0',
	`deck` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`score` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`flag` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`byes` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
	`status` TINYINT(1) UNSIGNED NOT NULL DEFAULT '0',
  `name` varchar(16) DEFAULT '0',
  PRIMARY KEY (`id`,`uid`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
